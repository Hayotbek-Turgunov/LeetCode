/**Write a function that reverses a string. The input string is given as an array of characters s.

You must do this by modifying the input array in-place with O(1) extra memory. */

/**Example 1:

Input: s = ["h","e","l","l","o"]
Output: ["o","l","l","e","h"]
Example 2:

Input: s = ["H","a","n","n","a","h"]
Output: ["h","a","n","n","a","H" */

/**Runtime 78 ms
Beats 60.07%

Memory 55.37 MB
Beats 96.79%
 */

var reverseString = function (s) {
  return s.reverse();
};

reverseString();
